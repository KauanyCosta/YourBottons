    public void carregaDadosUsuario() {
        int posicao = cmbUsuario.getSelectedIndex();
        Usuario usr = listaUsuarios.get(posicao);

        this.codigo = usr.getCodUsuario();
        jTFNome.setText(usr.getNomeusuario());
        jTFCPF.setText(usr.getCpf());
        jTFEmail.setText(usr.getEmail());
        jTFLogin.setText(usr.getLogin());
        JPFsenha.setText("");
        int tipo = usr.getTipo();
        jRBAtivo.setSelected(true);

        if (tipo == 1) {
            jRBSim.setSelected(true);
        } else {
            JRBNao.setSelected(true);
        }
    }

