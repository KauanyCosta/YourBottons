    private void jBSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSalvarActionPerformed
        if (!jTFNome.equals("")) {
            if (!jTFLogin.equals("")) {
//                if (!JPFsenha.equals("")) {
                if (!jTFEmail.equals("")) {
                    if (!jTFCPF.equals("")) {
                        if (jRBSim.isSelected() || JRBNao.isSelected()) {
                            if (jRBAtivo.isSelected() || JRBDesativo.isSelected()) {

                                String nome = jTFNome.getText();
                                String login = jTFLogin.getText();
                                String email = jTFEmail.getText();
                                String cpf = jTFCPF.getText();
                                String senha = JPFsenha.getText();

                                // metodo para desativar/excluir
                                if (JRBDesativo.isSelected()) {
                                    if (JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir?",
                                            this.getTitle(), JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {

                                        int posicao = cmbUsuario.getSelectedIndex();
                                        Usuario usr = listaUsuarios.get(posicao);
                                        if (usr != null) {
                                            BottonsCliente.ccont.excluirUsuario(usr);
                                            JOptionPane.showMessageDialog(this, "Usuário excluído com sucesso!", this.getTitle(),
                                                    JOptionPane.INFORMATION_MESSAGE);
                                            carregaCombo();

                                        } else {
                                            JOptionPane.showMessageDialog(this, "Erro ao excluir usuário!", this.getTitle(),
                                                    JOptionPane.ERROR_MESSAGE);
                                        }
                                    }

                                    //alterar usuario
                                } else if (JOptionPane.showConfirmDialog(this, "Tem certeza que deseja alterar?",
                                        this.getTitle(), JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {

                                    if (jRBSim.isSelected()) {
                                        tipo = 1;
                                    } else if (JRBNao.isSelected()) {
                                        tipo = 2;
                                    }

                                    int posicao = cmbUsuario.getSelectedIndex();
                                    Usuario usr = listaUsuarios.get(posicao);
                                    // ???????????????????????
                                    usr = new Usuario(codigo, nome, login, senha, email, cpf, tipo);
                                    boolean ok = BottonsCliente.ccont.alterarUsuario(usr);
                                    System.out.println(usr);

                                    if (ok) {
                                        JOptionPane.showMessageDialog(this, "Usuário alterado com sucesso", this.getTitle(),
                                                JOptionPane.INFORMATION_MESSAGE);
                                        carregaDadosUsuario();

                                    } else {
                                        JOptionPane.showMessageDialog(this, "Erro ao alterar usuário", this.getTitle(),
                                                JOptionPane.ERROR_MESSAGE);

                                    }

                                }

                            } else {
                                JOptionPane.showMessageDialog(this, "Selecione um estado!", this.getTitle(), JOptionPane.ERROR_MESSAGE);
                            }
                        } else {
                            JOptionPane.showMessageDialog(this, "Selecione um tipo!", this.getTitle(), JOptionPane.ERROR_MESSAGE);
                        }

                    } else {
                        JOptionPane.showMessageDialog(this, "Prencha o campo: CPF!", this.getTitle(), JOptionPane.ERROR_MESSAGE);
                        jTFCPF.requestFocus();
                    }

                } else {
                    JOptionPane.showMessageDialog(this, "Prencha o campo: Email!", this.getTitle(), JOptionPane.ERROR_MESSAGE);
                    jTFEmail.requestFocus();
                }
//
//                } else {
//                    JOptionPane.showMessageDialog(this, "Prencha o campo: Senha!", this.getTitle(), JOptionPane.ERROR_MESSAGE);
//                    JPFsenha.requestFocus();
//                }
            } else {
                JOptionPane.showMessageDialog(this, "Prencha o campo: Login!", this.getTitle(), JOptionPane.ERROR_MESSAGE);
                jTFLogin.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Prencha o campo: Nome!", this.getTitle(), JOptionPane.ERROR_MESSAGE);
            jTFNome.requestFocus();
        }

    }//GEN-LAST:event_jBSalvarActionPerformed
